  function resolveGroupCode(group) {
    if (group && group.code && LIB[group.code]) return group.code;
    const k = norm([group?.name, group?.short_name].filter(Boolean).join(' '));
    if (k.includes('ηλεκτρ')) return 'electrical';
    if (k.includes('οικοδομ') || k.includes('δομικ')) return 'building';
    if (k.includes('αδραν')) return 'aggregates';
    if (k.includes('ασφαλ')) return 'asphalt';
    if (k.includes('κιγκαλ') || k.includes('σιδηρ') || k.includes('αλουμιν')) return 'hardware';
    if (k.includes('κλιματ')) return 'air_conditioning';
    if (k.includes('υδραυλ') || k.includes('σωλην')) return 'plumbing';
    if (k.includes('ελαιοχρ') || k.includes('χρωμα') || k.includes('μονωτικ')) return 'paint';
    if (k.includes('σημαν')) return 'signage';
    return 'building';
  }

